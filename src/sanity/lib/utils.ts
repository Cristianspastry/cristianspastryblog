  // Funzione per ottenere tutti gli ingredienti con categoria
  export const getAllIngredientsWithCategory = () => {
    const allIngredients: {
      title: string;
      value: string;
      category: string;
      categoryName: string;
    }[] = [];
    
    Object.entries(pastryIngredients).forEach(([category, ingredients]) => {
      const categoryEmojis = {
        farine: '🌾',
        dolcificanti: '🍯',
        grassi: '🧈',
        liquidi: '🥛',
        uova: '🥚',
        lieviti: '🧁',
        cioccolato: '🍫',
        fruttaSecca: '🌰',
        fruttaFresca: '🍓',
        aromi: '✨',
        saleAcidi: '🧂',
        gelatine: '🍮',
        creme: '🥄',
        decorazioni: '🎂',
        conserve: '🍯'
      }
      
      const categoryNames = {
        farine: 'Farine e cereali',
        dolcificanti: 'Dolcificanti',
        grassi: 'Grassi',
        liquidi: 'Liquidi',
        uova: 'Uova e derivati',
        lieviti: 'Lieviti',
        cioccolato: 'Cioccolato e cacao',
        fruttaSecca: 'Frutta secca',
        fruttaFresca: 'Frutta fresca',
        aromi: 'Aromi e spezie',
        saleAcidi: 'Sale e acidi',
        gelatine: 'Gelatine e addensanti',
        creme: 'Creme e preparazioni',
        decorazioni: 'Decorazioni',
        conserve: 'Confetture e conserve'
      }
      
      ingredients.forEach(ingredient => {
        const emoji = categoryEmojis[category as keyof typeof categoryEmojis];
        const name = categoryNames[category as keyof typeof categoryNames];
        allIngredients.push({
          title: `${emoji} ${ingredient}`,
          value: ingredient,
          category: category,
          categoryName: name
        })
      })
      })
    
    
    return allIngredients
  }
  
